{
	return (isatty(STDIN_FILENO) && info->readfd <= 2);
}
/**
 * is_delim - a function that checks if character is a delimeter
 * @c: the character to be checked
 * @delim: the delimiter string where the character will be searched.
 * Return: 1 if true, 0 if false
 */
int is_delim(char c, char *delim)
{
	while (*delim)
		if (*delim++ == c)
			return (1);
	return (0);
}
/**
 *_isalpha - fucntion that checks for alphabetic character
 *@c: The character to be input
 *Return: 1 if c is alphabetic, 0 otherwise
 */

int _isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}

/**
 *_atoi - This function converts a given string into an integer.
 *@s: The input string that needs to be converted.
 *Return: If there are no numbers in the string, it returns 0. Otherwise, it returns the converted integer value.
 */

int _atoi(char *s)
{
	int i, sign = 1, flag = 0, output;
	unsigned int result = 0;

	for (i = 0;  s[i] != '\0' && flag != 2; i++)
	{
		if (s[i] == '-')
			sign *= -1;

		if (s[i] >= '0' && s[i] <= '9')
		{
			flag = 1;
			result *= 10;
			result += (s[i] - '0');
		}
		else if (flag == 1)
			flag = 2;
	}

	if (sign == -1)
		output = -result;
	else
		output = result;

	return (output);
}
